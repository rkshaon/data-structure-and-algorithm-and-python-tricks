# Sliding Window Technique
# Aims to reduce the use of nested loop and replace i single loop, thereby reducing the time complexity.
# The `window` is a subset of the data, and `slides` through the larger data structure, performing
# operation on each subset of the elements as it moves.

# Prerequisite
# Where size of the window for computation is fixed

# How to use
# 1. Find the size of window required
# 2. Complete the result for 1st window
# 3. Then use a loop to slide the window by 1, and loop computing the result
# window by window.

